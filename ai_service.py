_chat_rate: dict[str, list[float]] = {}
_CHAT_MAX_MESSAGES = 30  # максимум за окно
_CHAT_WINDOW_SEC = 600  # 10 минут


def _check_rate_limit(session_id: str) -> bool:
    """True = разрешено, False = превышен лимит."""
    now = time.time()
    window_start = now - _CHAT_WINDOW_SEC
    timestamps = _chat_rate.get(session_id, [])
    timestamps = [t for t in timestamps if t > window_start]
    if len(timestamps) >= _CHAT_MAX_MESSAGES:
        _chat_rate[session_id] = timestamps
        return False
    timestamps.append(now)
    _chat_rate[session_id] = timestamps
    return True


