def _normalize_cyr(text: str) -> str:
    """
    Нормализует текст к «чистой» кириллице:
    1. lower
    2. цифры/спецсимволы → буквы
    3. латинские омоглифы → кириллица
    4. убираем все разделители
    """
    t = text.lower()
    # шаг 1: цифры и спецсимволы
    result = []
    for ch in t:
        result.append(_DIGIT_MAP.get(ch, ch))
    t = "".join(result)
    # шаг 2: латиница → кириллица
    t = t.translate(_LATIN_TO_CYR)
    # шаг 3: убираем разделители и не-буквы (кроме русских)
    t = _SEPARATORS_RE.sub("", t)
    return t


def _normalize_lat(text: str) -> str:
    """
    Нормализует текст к латинскому транслиту (для поиска транслит-мата).
    """
    t = text.lower()
    result = []
    for ch in t:
        result.append(_DIGIT_MAP.get(ch, ch))
    t = "".join(result)
    t = t.translate(_CYR_TO_LATIN)
    t = _SEPARATORS_RE.sub("", t)
    return t


def _is_toxic_local(text: str) -> bool:
    """
    Многоуровневая проверка:
    1. Нормализация к кириллице + regex-корни
    2. Латинские паттерны — только во фрагментах с латиницей (транслит/англ. мат)
    3. Точное совпадение токенов
    """
    norm_cyr = _normalize_cyr(text)

    # Проверка кириллических паттернов
    for pattern in _COMPILED_CYR:
        if pattern.search(norm_cyr):
            return True

    # Латинские паттерны нельзя применять ко всему тексту: при транслите кириллицы
    # «улучшить» → «uluchshit», «завершить» → «zavershit» — ложные срабатывания на «shit».
    latin_parts = [
        token
        for token in _SEPARATORS_RE.split(text.lower())
        if token and re.search(r"[a-zA-Z]", token)
    ]
    if latin_parts:
        norm_lat = _normalize_lat("".join(latin_parts))
        for pattern in _COMPILED_LAT:
            if pattern.search(norm_lat):
                return True

    # Точное совпадение токенов (слов) после кириллической нормализации
    tokens = re.split(r"\s+", _normalize_cyr(text.lower()))
    for tok in tokens:
        if tok in _EXACT_BAD_CYR:
            return True

    return False


