def moderate_content(text: str) -> dict:
    """
    Двухуровневая модерация:
    1. Локальный фильтр (быстрый, без API)
    2. AI-модерация (если локальный пропустил)
    """
    if _is_toxic_local(text):
        return _local_toxicity_result()

    messages = [
        {"role": "system", "content": _SYSTEM_MODERATOR},
        {"role": "user", "content": f"Проверь текст:\n\n{text}"},
    ]
    raw = _call_api(messages, temperature=0.0, max_tokens=200)

    if raw is None:
        return {
            "approved": True,
            "toxicity_score": 0.0,
            "reason": "Модерация API недоступна — опубликовано.",
        }

    match = re.search(r"\{[^{}]*\}", raw, re.DOTALL)
    if not match:
        return {
            "approved": True,
            "toxicity_score": 0.0,
            "reason": "Не удалось распознать ответ модератора.",
        }

    try:
        result = json.loads(match.group())
        result.setdefault("approved", True)
        result.setdefault("toxicity_score", 0.0)
        result.setdefault("reason", "")

        if result["toxicity_score"] >= Config.TOXICITY_THRESHOLD:
            result["approved"] = False

        return result
    except json.JSONDecodeError:
        return {
            "approved": True,
            "toxicity_score": 0.0,
            "reason": "Ошибка парсинга ответа модератора.",
        }


def chat_with_assistant(
    user_message: str, history: list[dict] | None = None, session_id: str = "default"
) -> tuple[str, bool]:
    """
    Возвращает (ответ, is_blocked).
    is_blocked=True если запрос отклонён по guardrails.
    """
    # 1. Rate limiting
    if not _check_rate_limit(session_id):
        return (
            "Вы отправляете слишком много сообщений. "
            "Пожалуйста, подождите несколько минут.",
            True,
        )

    # 2. Валидация входа
    ok, err = _validate_chat_input(user_message)
    if not ok:
        return err, True

    # 3. Запрос к API
    messages = [{"role": "system", "content": _SYSTEM_ASSISTANT}]
    if history:
        for item in history[-8:]:
            role = item.get("role", "user")
            content = item.get("message", "")
            if role in ("user", "assistant") and content:
                messages.append({"role": role, "content": content[:500]})
    messages.append({"role": "user", "content": user_message})

    answer = _call_api(messages, temperature=0.5, max_tokens=800)
    if answer:
        return answer, False

    return _api_unavailable_message(), False


