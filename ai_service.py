def _validate_chat_input(message: str) -> tuple[bool, str]:
    """
    Проверяет входящее сообщение для чата.
    Возвращает (ok, error_text).
    """
    if not message or not message.strip():
        return False, "Пустое сообщение."

    if len(message) > _MAX_CHAT_LEN:
        return False, f"Сообщение слишком длинное (максимум {_MAX_CHAT_LEN} символов)."

    if _is_toxic_local(message):
        return (
            False,
            "Сообщение содержит недопустимую лексику. Пожалуйста, общайтесь корректно.",
        )

    if _JAILBREAK_RE.search(message):
        return False, (
            "Я не могу обрабатывать такие запросы. "
            "Я ассистент по вопросам транспорта и логистики — чем могу помочь?"
        )

    if _OFFTOPIC_RE.search(message):
        return False, (
            "Этот вопрос выходит за рамки моей компетенции. "
            "Я специализируюсь на транспортной логистике, грузоперевозках и ТО транспорта. "
            "Задайте вопрос по этим темам!"
        )

    return True, ""


